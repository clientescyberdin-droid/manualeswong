// PROGRESO
function recalcularProgreso(){
  var grid=document.getElementById("progressGrid"); if(!grid) return;
  grid.innerHTML="";
  Object.keys(MINAS).forEach(function(sc){
    var temas=DOCS[sc]||[]; var total=0,cargados=0;
    temas.forEach(function(tema){
      var hidden=_hiddenItems[tema.code]||[];
      var all=tema.items.filter(function(it){ return hidden.indexOf(it.code)===-1; }).concat(_extraItems[tema.code]||[]);
      all.forEach(function(item){
        ["L","P","E","C"].forEach(function(t){ total++; if(_driveCache[item.code+"-"+t]) cargados++; });
      });
    });
    var pct=total>0?Math.round((cargados/total)*100):0;
    var fc=pct===100?"full":pct<20?"low":"";
    var card=document.createElement("div"); card.className="prog-card";
    card.innerHTML="<div class='prog-card-top'><span class='prog-card-name'>"+MINAS[sc]+"</span><span class='prog-card-pct'>"+pct+"%</span></div>"
      +"<div class='prog-bar-bg'><div class='prog-bar-fill "+fc+"' style='width:"+pct+"%'></div></div>"
      +"<div class='prog-card-detail'>"+cargados+" de "+total+" documentos</div>";
    grid.appendChild(card);
  });
}

