function subirMultiples(files,code,index){
  if(index>=files.length){ mostrarToast(files.length+" archivos subidos","ok"); recalcularProgreso(); return; }
  mostrarToast("Subiendo archivo "+(index+1)+" de "+files.length+"...","loading");
  subirDrive(files[index],code,function(){ subirMultiples(files,code,index+1); });
}
function subirDrive(file,code,onDone){
  var ext=file.name.substring(file.name.lastIndexOf("."));
  mostrarToast("Verificando versiones...","loading");
  fetch("https://www.googleapis.com/drive/v3/files?q=name+contains+%27"+encodeURIComponent(code)+"%27+and+%27"+FOLDER_ID+"%27+in+parents+and+trashed%3Dfalse&fields=files(id,name)&orderBy=createdTime",{headers:{Authorization:"Bearer "+_accessToken}})
  .then(function(r){ return r.json(); })
  .then(function(res){
    var existing=(res.files||[]).filter(function(f){ return f.name.indexOf(code)===0; });
    var version=existing.length+1;
    var nombre=code+"-v"+version+ext;
    var metadata=JSON.stringify({name:nombre,parents:[FOLDER_ID]});
    var form=new FormData();
    form.append("metadata",new Blob([metadata],{type:"application/json"}));
    form.append("file",file);
    return fetch("https://www.googleapis.com/upload/drive/v3/files?uploadType=multipart",{method:"POST",headers:{Authorization:"Bearer "+_accessToken},body:form});
  })
  .then(function(r){ return r.json(); })
  .then(function(data){
    if(data.id){
      _driveCache[code]=data.id;
      // Guardar tambien con version para tener historial
      _driveCache[code+"-v"+version]=data.id;
      guardarCache();
      return fetch("https://www.googleapis.com/drive/v3/files/"+data.id+"/permissions",{method:"POST",headers:{Authorization:"Bearer "+_accessToken,"Content-Type":"application/json"},body:JSON.stringify({role:"reader",type:"anyone"})})
      .then(function(){
        mostrarToast(data.name+" subido","ok");
        actualizarStatusDot(code,true); recalcularProgreso(); contarPendientes();
        if(onDone) onDone();
      });
    } else { mostrarToast("Error: "+(data.error?data.error.message:"desconocido"),"err"); }
  })
  .catch(function(e){ mostrarToast("Error de conexion.","err"); console.error(e); });
}
function descargarArchivo(e,code){
  e.preventDefault();
  // Buscar en cache local primero (no requiere auth)
  if(_driveCache[code]){
    window.open("https://drive.google.com/file/d/"+_driveCache[code]+"/view?usp=sharing","_blank");
    mostrarToast("Abriendo archivo...","ok");
    return;
  }
  // Si hay versiones en cache de versiones
  var versiones = [];
  var keys = Object.keys(_driveCache);
  keys.forEach(function(k){
    if(k.indexOf(code) === 0) versiones.push({code:k, id:_driveCache[k]});
  });
  if(versiones.length === 1){
    window.open("https://drive.google.com/file/d/"+versiones[0].id+"/view?usp=sharing","_blank");
    mostrarToast("Abriendo archivo...","ok");
    return;
  }
  if(versiones.length > 1){
    // Mostrar lista de versiones del cache
    var filesFromCache = versiones.map(function(v){
      return {id:v.id, name:v.code, size:"", mimeType:"application/pdf", createdTime:""};
    });
    mostrarModalArchivos(code, filesFromCache);
    return;
  }
  // No hay en cache - necesita auth para buscar en Drive
  if(!_accessToken){
    mostrarToast("Conectando con Google Drive para buscar el archivo...","loading");
    _pendingCode=code; _pendingDownload=true;
    autenticar(); return;
  }
  buscarYDescargar(code);
}
function buscarYDescargar(code){
  mostrarToast("Buscando archivos...","loading");
  fetch("https://www.googleapis.com/drive/v3/files?q=name+contains+%27"+encodeURIComponent(code)+"%27+and+%27"+FOLDER_ID+"%27+in+parents+and+trashed%3Dfalse&fields=files(id,name,size,mimeType,createdTime)&orderBy=createdTime+desc",{headers:{Authorization:"Bearer "+_accessToken}})
  .then(function(r){ return r.json(); })
  .then(function(res){
    var files=(res.files||[]).filter(function(f){ return f.name.indexOf(code)===0; });
    if(files.length===0){
      mostrarToast("Archivo no encontrado. Cargalo primero.","err"); return;
    }
    if(files.length===1){
      window.open("https://drive.google.com/file/d/"+files[0].id+"/view?usp=sharing","_blank");
      mostrarToast("Archivo abierto","ok"); return;
    }
    mostrarModalArchivos(code,files);
  })
  .catch(function(){ mostrarToast("Error de conexion con Drive.","err"); });
}
